    class password
    {
        int _min;
        int _max;
        int _c;
        string _pw;
        private bool _valid;


        public password(int min, int max, char c, string pw)
        {
            _min = min;
            _max = max;
            _c = c;
            _pw = pw;
            _valid = false;
            verify2();
        }

        public bool Valid { get => _valid; }

        private void verify1()
        {
            var count = _pw.Count(x => x == _c);
            if (count >= _min && count <= _max) _valid = true;
        }

        private void verify2()
        {
            bool min = (_pw[_min - 1] == _c);
            bool max = (_pw[_max - 1] == _c);

            if ((min && !max) || (!min && max)) _valid = true;
        }
    }
